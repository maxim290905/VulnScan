/**
 * @file fupload.h
 * @brief Модуль для тестирования уязвимостей при загрузке файлов
 *
 * Пытается загрузить различные полезные нагрузки, чтобы обнаружить
 * уязвимости загрузки файлов: обход фильтров типов, инъекции
 * в имена файлов, загрузку исполняемых файлов и др.
 *
 * @author Усольцев, Ялфимов, Королев
 * @date 2025-06-13
 */
#ifndef FUPLOAD_H
#define FUPLOAD_H

#include <string>

/**
 * @brief Выполняет тестирование уязвимостей загрузки файлов на указанном URL.
 *
 * Пробует различные полезные нагрузки (PDF, EXE, PHP-shell, двойные расширения,
 * XSS в именах файлов) и сообщает, была ли загрузка принята или заблокирована.
 *
 * @param url Целевой URL для тестирования загрузки файлов.
 */
void upload_file(const std::string& url);

#endif // FUPLOAD_H